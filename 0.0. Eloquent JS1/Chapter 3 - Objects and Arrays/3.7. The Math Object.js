//The Math Object

/*
    All the trigonometric functions are there—Math.cos,
    sin, tan, acos, asin, atan. π and e are there, 
    written in all capital letters (Math.PI and Math.E)
*/

/*
    Math.max and Math.min can give the maximum or minimum of two values. 
    Math.round, Math.floor, and Math.ceil will round numbers 
    to the closest whole number, the whole number below it,
    and the one above it, respectively.
*/

var number = 16;
var number2 = 2;
var number3 = 2.5;

console.log(Math.min(number, number2));
console.log(Math.max(number, number2));

console.log(Math.pow(number, number2));
console.log(Math.sqrt(number));

console.log(Math.round(number3));
console.log(Math.floor(number3));
console.log(Math.ceil(number3));
