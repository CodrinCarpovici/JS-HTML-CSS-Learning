//Sum of two Numbers

function sumOf(no1, no2) {
    return no1 + no2;
}

console.log(sumOf(1, 2));

//Average of 3 integers

function averageOf(no1, no2, no3) {
    return (no1 + no2 + no3)/3;
}

console.log(averageOf(1, 2, 3));

//Area of a circle

function circleA(r) {
    return Math.PI * Math.pow(r, 2);
}

console.log(circleA(3));

//Circumference of a circle

function circleC(r) {
    return 2 * Math.PI * r;
}

console.log(circleC(3));